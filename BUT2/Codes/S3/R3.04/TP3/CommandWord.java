/**
 * The CommandWord enum represents the valid command words that can be used in the game.
 * It includes the following command words: GO, QUIT, HELP, LOOK, and UNKNOWN.
 */
public enum CommandWord {
    /**
     * Command to move in the game.
     */
    GO,
    
    /**
     * Command to quit the game.
     */
    QUIT,
    
    /**
     * Command to get help.
     */
    HELP,
    
    /**
     * Command to look around in the game.
     */
    LOOK,
    
    /**
     * Unknown or unrecognised command.
     */
    UNKNOWN
}

